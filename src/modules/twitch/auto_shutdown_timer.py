def update_auto_shutdown_timer(time):
    """
    –û–±–Ω–æ–≤–ª—è–µ—Ç –ø–∞—Ä–∞–º–µ—Ç—Ä 'auto_shutdown_timer' –≤ –Ω–∞—Å—Ç—Ä–æ–π–∫–∞—Ö 'BROWSER_SETTINGS'.
    """
    try:
        # –ó–∞–≥—Ä—É–∂–∞–µ–º —Ç–µ–∫—É—â–∏–µ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏
        logger.info(f"üîÑ –ó–∞–≥—Ä—É–∑–∫–∞ –Ω–∞—Å—Ç—Ä–æ–µ–∫ '{BROWSER_SETTINGS}'...")  # –õ–æ–≥–∏—Ä—É–µ–º –ø—Ä–æ—Ü–µ—Å—Å –ø–æ–ª—É—á–µ–Ω–∏—è –Ω–∞—Å—Ç—Ä–æ–µ–∫

        # –ß–∏—Ç–∞–µ–º —Ç–µ–∫—É—â–∏–µ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ –∏–∑ —Ñ–∞–π–ª–∞
        settings_path = get_settings_path()  # –ü—É—Ç—å –∫ —Ñ–∞–π–ª—É –Ω–∞—Å—Ç—Ä–æ–µ–∫
        with open(settings_path, 'r') as file:
            settings = json.load(file)  # –ó–∞–≥—Ä—É–∂–∞–µ–º –≤—Å–µ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é

        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ 'BROWSER_SETTINGS' —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –≤ –Ω–∞—Å—Ç—Ä–æ–π–∫–∞—Ö
        if "BROWSER_SETTINGS" not in settings:
            error_message = "'BROWSER_SETTINGS' –Ω–µ –Ω–∞–π–¥–µ–Ω—ã –≤ –Ω–∞—Å—Ç—Ä–æ–π–∫–∞—Ö."
            logger.error(f"‚ùå {error_message}")
            raise ValueError(error_message)

        # –û–±–Ω–æ–≤–ª—è–µ–º –ø–∞—Ä–∞–º–µ—Ç—Ä 'auto_shutdown_timer' –≤ —Å–µ–∫—Ü–∏–∏ 'BROWSER_SETTINGS'
        settings["BROWSER_SETTINGS"]["AUTO_SHUTDOWN_TIMER"] = time

        # –ó–∞–ø–∏—Å—ã–≤–∞–µ–º –∏–∑–º–µ–Ω–µ–Ω–∏—è –æ–±—Ä–∞—Ç–Ω–æ –≤ settings.json
        with open(settings_path, 'w') as file:
            json.dump(settings, file, indent=4)  # –°–æ—Ö—Ä–∞–Ω—è–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–Ω—ã–µ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ –≤ JSON

        logger.info(f"‚úÖ –ü–∞—Ä–∞–º–µ—Ç—Ä 'auto_shutdown_timer' –æ–±–Ω–æ–≤–ª–µ–Ω: {time} —Å–µ–∫—É–Ω–¥.")  # –õ–æ–≥–∏—Ä—É–µ–º —É—Å–ø–µ—à–Ω–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ

    except Exception as e:
        logger.error(f"‚ö†Ô∏è –û—à–∏–±–∫–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –Ω–∞—Å—Ç—Ä–æ–µ–∫: {e}", exc_info=True)
        raise
