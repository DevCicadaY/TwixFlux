                for cookie in cookies:
                    logger.debug(f"üîç –ü—Ä–æ–≤–µ—Ä—è–µ–º cookie: {cookie.name} –≤ –¥–æ–º–µ–Ω–µ {cookie.domain}")  # –õ–æ–≥–∏—Ä—É–µ–º –ø—Ä–æ–≤–µ—Ä–∫—É –∫–∞–∂–¥–æ–≥–æ cookie
                    if cookie.name == "auth-token" and "twitch.tv" in cookie.domain:  # –ò—â–µ–º cookie —Å –Ω—É–∂–Ω—ã–º –∏–º–µ–Ω–µ–º
                        logger.info("‚úÖ –£—Å–ø–µ—à–Ω–æ –Ω–∞–π–¥–µ–Ω auth-token –¥–ª—è Twitch.")  # –õ–æ–≥–∏—Ä—É–µ–º, –µ—Å–ª–∏ —Ç–æ–∫–µ–Ω –Ω–∞–π–¥–µ–Ω
