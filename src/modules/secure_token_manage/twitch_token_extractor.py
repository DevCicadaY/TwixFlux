def get_browser_cookies(browser_name):  # –§—É–Ω–∫—Ü–∏—è –∏–∑–≤–ª–µ—á–µ–Ω–∏—è cookies –ø–æ –∏–º–µ–Ω–∏ –±—Ä–∞—É–∑–µ—Ä–∞
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç cookies –¥–ª—è –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ –±—Ä–∞—É–∑–µ—Ä–∞. üç™"""
    try:
        logger.info(f"üîç –ò–∑–≤–ª–µ–∫–∞–µ–º cookies –∏–∑ –±—Ä–∞—É–∑–µ—Ä–∞: {browser_name}...")  # –õ–æ–≥–∏—Ä—É–µ–º –ø—Ä–æ—Ü–µ—Å—Å –∏–∑–≤–ª–µ—á–µ–Ω–∏—è cookies

        # –ü–æ–ª—É—á–∞–µ–º cookies –¥–ª—è —Ä–∞–∑–Ω—ã—Ö –±—Ä–∞—É–∑–µ—Ä–æ–≤ üßë‚Äçüíª
        if browser_name == "chrome":
            cookies = browser_cookie3.chrome()  # –ò–∑–≤–ª–µ–∫–∞–µ–º cookies –¥–ª—è –±—Ä–∞—É–∑–µ—Ä–∞ Chrome
        elif browser_name == "chromium":
            cookies = browser_cookie3.chromium()  # –ò–∑–≤–ª–µ–∫–∞–µ–º cookies –¥–ª—è –±—Ä–∞—É–∑–µ—Ä–∞ Chromium
        elif browser_name == "firefox":
            cookies = browser_cookie3.firefox()  # –ò–∑–≤–ª–µ–∫–∞–µ–º cookies –¥–ª—è –±—Ä–∞—É–∑–µ—Ä–∞ Firefox
        elif browser_name == "edge":
            cookies = browser_cookie3.edge()  # –ò–∑–≤–ª–µ–∫–∞–µ–º cookies –¥–ª—è –±—Ä–∞—É–∑–µ—Ä–∞ Edge
        elif browser_name == "opera":
            cookies = browser_cookie3.opera()  # –ò–∑–≤–ª–µ–∫–∞–µ–º cookies –¥–ª—è –±—Ä–∞—É–∑–µ—Ä–∞ Opera
        elif browser_name == "arc":
            cookies = browser_cookie3.arc()  # –ò–∑–≤–ª–µ–∫–∞–µ–º cookies –¥–ª—è –±—Ä–∞—É–∑–µ—Ä–∞ Arc
        elif browser_name == "brave":
            cookies = browser_cookie3.brave()  # –ò–∑–≤–ª–µ–∫–∞–µ–º cookies –¥–ª—è –±—Ä–∞—É–∑–µ—Ä–∞ Brave
        else:
            logger.warning(f"‚ö†Ô∏è –ü–æ–¥–¥–µ—Ä–∂–∫–∞ –¥–ª—è –±—Ä–∞—É–∑–µ—Ä–∞ {browser_name} –Ω–µ —Ä–µ–∞–ª–∏–∑–æ–≤–∞–Ω–∞.")  # –õ–æ–≥–∏—Ä—É–µ–º, –µ—Å–ª–∏ –±—Ä–∞—É–∑–µ—Ä –Ω–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç—Å—è
            return None

        if cookies:  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –±—ã–ª–∏ –ª–∏ –∏–∑–≤–ª–µ—á–µ–Ω—ã cookies
            logger.info(f"‚úÖ –£—Å–ø–µ—à–Ω–æ –∏–∑–≤–ª–µ—á–µ–Ω—ã {len(cookies)} cookies –∏–∑ {browser_name}.")  # –õ–æ–≥–∏—Ä—É–µ–º —É—Å–ø–µ—à–Ω–æ–µ –∏–∑–≤–ª–µ—á–µ–Ω–∏–µ
            return cookies  # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–ø–∏—Å–æ–∫ cookies
        else:
            logger.warning(f"‚ö†Ô∏è –ù–µ —É–¥–∞–ª–æ—Å—å –∏–∑–≤–ª–µ—á—å cookies –∏–∑ {browser_name}.")  # –õ–æ–≥–∏—Ä—É–µ–º, –µ—Å–ª–∏ cookies –Ω–µ –∏–∑–≤–ª–µ—á–µ–Ω—ã
            return None
    except Exception as e:  # –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—à–∏–±–æ–∫ –ø—Ä–∏ –∏–∑–≤–ª–µ—á–µ–Ω–∏–∏ cookies
        logger.error(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –∏–∑–≤–ª–µ—á–µ–Ω–∏–∏ cookies –∏–∑ {browser_name}: {e}", exc_info=True)  # –õ–æ–≥–∏—Ä—É–µ–º –æ—à–∏–±–∫—É
