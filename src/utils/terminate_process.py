def find_process_by_name(process_name):
    """–ò—â–µ–º –ø—Ä–æ—Ü–µ—Å—Å—ã –ø–æ –∏–º–µ–Ω–∏ —á–µ—Ä–µ–∑ pgrep"""
    try:
        # –í—ã–ø–æ–ª–Ω—è–µ–º –∫–æ–º–∞–Ω–¥—É pgrep —Å —Ñ–ª–∞–≥–æ–º -f –¥–ª—è –ø–æ–∏—Å–∫–∞ –≤—Å–µ—Ö –ø—Ä–æ—Ü–µ—Å—Å–æ–≤ –ø–æ –∏–º–µ–Ω–∏
        result = subprocess.check_output(["pgrep", "-f", process_name]).decode("utf-8")  # –ü–æ–ª—É—á–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫–∏
        # –†–∞–∑–±–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É –Ω–∞ —Å—Ç—Ä–æ–∫–∏ –∏ –∫–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ–º –∏—Ö –≤ —Å–ø–∏—Å–æ–∫ —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª (PID)
        return [int(pid) for pid in result.splitlines()]
    except subprocess.CalledProcessError:
        # –ï—Å–ª–∏ pgrep –Ω–µ –Ω–∞—à–µ–ª –ø—Ä–æ—Ü–µ—Å—Å—ã, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –ø—É—Å—Ç–æ–π —Å–ø–∏—Å–æ–∫
        return []

def terminate_processes():
    """–ó–∞–≤–µ—Ä—à–∞–µ–º –≤—Å–µ –ø—Ä–æ—Ü–µ—Å—Å—ã —Å –∏–º–µ–Ω–∞–º–∏ process_name_1 –∏ process_name_2"""
    # –°–ø–∏—Å–æ–∫ –ø—Ä–æ—Ü–µ—Å—Å–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ –∑–∞–≤–µ—Ä—à–∏—Ç—å
    process_names = ["TwixFlux", "Python"]  # –ò–º–µ–Ω–∞ –ø—Ä–æ—Ü–µ—Å—Å–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –º—ã –∏—â–µ–º –∏ –∑–∞–≤–µ—Ä—à–∞–µ–º
    
    # –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –∫–∞–∂–¥–æ–º—É –∏–º–µ–Ω–∏ –ø—Ä–æ—Ü–µ—Å—Å–∞ –∏–∑ —Å–ø–∏—Å–∫–∞
    for process_name in process_names:
        # –ò—â–µ–º –ø—Ä–æ—Ü–µ—Å—Å—ã —Å –¥–∞–Ω–Ω—ã–º –∏–º–µ–Ω–µ–º —Å –ø–æ–º–æ—â—å—é —Ñ—É–Ω–∫—Ü–∏–∏ find_process_by_name
        pids = find_process_by_name(process_name)
        
        # –ï—Å–ª–∏ –ø—Ä–æ—Ü–µ—Å—Å—ã –Ω–∞–π–¥–µ–Ω—ã
        if pids:
            # –õ–æ–≥–∏—Ä—É–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö –ø—Ä–æ—Ü–µ—Å—Å–∞—Ö –∏ –∏—Ö PID
            logger.info(f"‚úÖ –ù–∞–π–¥–µ–Ω—ã –ø—Ä–æ—Ü–µ—Å—Å—ã {process_name}: {pids}")
            
            # –î–ª—è –∫–∞–∂–¥–æ–≥–æ PID –∏–∑ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö –ø—Ä–æ—Ü–µ—Å—Å–æ–≤
            for pid in pids:
                # –õ–æ–≥–∏—Ä—É–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ø—Ä–æ—Ü–µ—Å—Å–µ, –∫–æ—Ç–æ—Ä—ã–π –±—É–¥–µ–º –∑–∞–≤–µ—Ä—à–∞—Ç—å
                logger.info(f"üõë –ó–∞–≤–µ—Ä—à–∞–µ–º –ø—Ä–æ—Ü–µ—Å—Å —Å PID: {pid}")
                # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø—Ä–æ—Ü–µ—Å—Å—É —Å–∏–≥–Ω–∞–ª –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è (SIGTERM)
                os.kill(pid, signal.SIGTERM)  # –ó–∞–≤–µ—Ä—à–∞–µ–º –ø—Ä–æ—Ü–µ—Å—Å —Å —É–∫–∞–∑–∞–Ω–Ω—ã–º PID
                # –õ–æ–≥–∏—Ä—É–µ–º —É—Å–ø–µ—à–Ω–æ–µ –∑–∞–≤–µ—Ä—à–µ–Ω–∏–µ –ø—Ä–æ—Ü–µ—Å—Å–∞
                logger.info(f"‚úÖ –ü—Ä–æ—Ü–µ—Å—Å —Å PID {pid} —É—Å–ø–µ—à–Ω–æ –∑–∞–≤–µ—Ä—à–µ–Ω.")
        else:
            # –ï—Å–ª–∏ –ø—Ä–æ—Ü–µ—Å—Å—ã —Å –¥–∞–Ω–Ω—ã–º –∏–º–µ–Ω–µ–º –Ω–µ –Ω–∞–π–¥–µ–Ω—ã, –ª–æ–≥–∏—Ä—É–µ–º –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ
            logger.warning(f"‚ö†Ô∏è –ü—Ä–æ—Ü–µ—Å—Å {process_name} –Ω–µ –Ω–∞–π–¥–µ–Ω.")

if __name__ == "__main__":  # –ì–ª–∞–≤–Ω–∞—è —Ç–æ—á–∫–∞ –≤—Ö–æ–¥–∞ –≤ –ø—Ä–æ–≥—Ä–∞–º–º—É
    driver = None  # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é driver –¥–ª—è WebDriver (–µ—Å–ª–∏ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –≤ –ø—Ä–æ–µ–∫—Ç–µ)
    
