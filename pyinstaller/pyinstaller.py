def create_icns_from_png():
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –ø—É—Ç–∏ –¥–ª—è —Ñ–∞–π–ª–æ–≤ –∏ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–π
    script_dir = os.path.dirname(os.path.realpath(sys.argv[0]))  # –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è —Å–∫—Ä–∏–ø—Ç–∞
    source_icon_path = 'icon.png'  # –ü—É—Ç—å –∫ –∏—Å—Ö–æ–¥–Ω–æ–º—É —Ñ–∞–π–ª—É –∏–∫–æ–Ω–∫–∏
    iconset_dir = 'TwixFlux.iconset'  # –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –ø—Ä–æ–º–µ–∂—É—Ç–æ—á–Ω—ã—Ö –∏–∫–æ–Ω–æ–∫
    icns_file = 'TwixFlux.icns'  # –§–∏–Ω–∞–ª—å–Ω—ã–π —Ñ–∞–π–ª .icns
    app_resource_dir = '../dist/TwixFlux.app/Contents/Resources'  # –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è –¥–ª—è —Ä–µ—Å—É—Ä—Å–æ–≤ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è

    # –ü–µ—Ä–µ—Ö–æ–¥ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é —Å–∫—Ä–∏–ø—Ç–∞
    os.chdir(script_dir)

    # –®–∞–≥ 1: –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è –∏—Å—Ö–æ–¥–Ω–æ–≥–æ —Ñ–∞–π–ª–∞ –∏–∫–æ–Ω–∫–∏
    if not os.path.exists(source_icon_path):
        logger.error("‚ùå –ò—Å—Ö–æ–¥–Ω—ã–π —Ñ–∞–π–ª –∏–∫–æ–Ω–∫–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω.")  # –õ–æ–≥–∏—Ä—É–µ–º –æ—à–∏–±–∫—É, –µ—Å–ª–∏ —Ñ–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω
        exit(1)  # –ó–∞–≤–µ—Ä—à–∞–µ–º –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ, –µ—Å–ª–∏ —Ñ–∞–π–ª –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç

    # –°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –¥–ª—è –∏–∫–æ–Ω–æ–∫, –µ—Å–ª–∏ –æ–Ω–∞ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
    if not os.path.exists(iconset_dir):
        os.makedirs(iconset_dir)  # –°–æ–∑–¥–∞–µ–º –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –¥–ª—è –∏–∫–æ–Ω–æ–∫
        logger.info(f"‚úÖ –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è –¥–ª—è –∏–∫–æ–Ω–æ–∫ —Å–æ–∑–¥–∞–Ω–∞.")  # –õ–æ–≥–∏—Ä—É–µ–º —É—Å–ø–µ—à–Ω–æ–µ —Å–æ–∑–¥–∞–Ω–∏–µ

    # –ú–∞—Å—Å–∏–≤ —Ä–∞–∑–º–µ—Ä–æ–≤ –∏–∫–æ–Ω–æ–∫ –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è —Ä–∞–∑–Ω—ã—Ö –≤–µ—Ä—Å–∏–π
    sizes = [16, 32, 64, 128, 256, 512, 1024]
    for size in sizes:
        for scale in [1, 2]:  # –ú–∞—Å—à—Ç–∞–±–∏—Ä–æ–≤–∞–Ω–∏–µ –∏–∫–æ–Ω–æ–∫ –¥–ª—è —Ä–∞–∑–Ω—ã—Ö –ø–ª–æ—Ç–Ω–æ—Å—Ç–µ–π –ø–∏–∫—Å–µ–ª–µ–π
            new_size = size * scale  # –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º –Ω–æ–≤—ã–π —Ä–∞–∑–º–µ—Ä
            output_file = os.path.join(iconset_dir, f'icon_{new_size}x{new_size}.png')  # –ü—É—Ç—å –¥–ª—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è –∏–∫–æ–Ω–∫–∏

            # –ú–∞—Å—à—Ç–∞–±–∏—Ä—É–µ–º –∏–∫–æ–Ω–∫—É —Å –ø–æ–º–æ—â—å—é —É—Ç–∏–ª–∏—Ç—ã sips
            subprocess.run(
                ['sips', '--resampleHeightWidth', str(new_size), str(new_size), source_icon_path, '--out', output_file],
                stdout=subprocess.DEVNULL,  # –û—Ç–∫–ª—é—á–∞–µ–º –≤—ã–≤–æ–¥ –≤ –∫–æ–Ω—Å–æ–ª—å
                stderr=subprocess.DEVNULL   # –û—Ç–∫–ª—é—á–∞–µ–º –≤—ã–≤–æ–¥ –æ—à–∏–±–æ–∫
            )
            
            # –õ–æ–≥–∏—Ä—É–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Ä–∞–∑–º–µ—Ä–µ –∏–∫–æ–Ω–∫–∏
            logger.info(f"üñºÔ∏è –ò–∫–æ–Ω–∫–∞ {new_size}x{new_size} —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–∞.")

    # –®–∞–≥ 2: –°–æ–∑–¥–∞–Ω–∏–µ .icns —Ñ–∞–π–ª–∞ –∏–∑ –∏–∫–æ–Ω–æ–∫
    subprocess.run(['iconutil', '--convert', 'icns', iconset_dir, '--output', icns_file])
    if os.path.exists(icns_file):
        logger.info(f"‚úÖ –§–∞–π–ª {icns_file} —É—Å–ø–µ—à–Ω–æ —Å–æ–∑–¥–∞–Ω.")  # –õ–æ–≥–∏—Ä—É–µ–º —É—Å–ø–µ—à–Ω–æ–µ —Å–æ–∑–¥–∞–Ω–∏–µ .icns —Ñ–∞–π–ª–∞
    else:
        logger.error(f"‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å —Å–æ–∑–¥–∞—Ç—å {icns_file}.")  # –õ–æ–≥–∏—Ä—É–µ–º –æ—à–∏–±–∫—É, –µ—Å–ª–∏ —Ñ–∞–π–ª –Ω–µ —Å–æ–∑–¥–∞–Ω
        exit(1)  # –ó–∞–≤–µ—Ä—à–∞–µ–º –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ, –µ—Å–ª–∏ –Ω–µ —É–¥–∞–ª–æ—Å—å —Å–æ–∑–¥–∞—Ç—å —Ñ–∞–π–ª

