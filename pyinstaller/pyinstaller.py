            shutil.rmtree(build_dir)  # –£–¥–∞–ª—è–µ–º –ø–∞–ø–∫—É
            logger.info(f'‚úÖ –ü–∞–ø–∫–∞ {build_dir} —É—Å–ø–µ—à–Ω–æ —É–¥–∞–ª–µ–Ω–∞.')
