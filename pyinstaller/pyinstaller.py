def start_first_build(target_script):
    """
    –í—ã–ø–æ–ª–Ω—è–µ—Ç –ø–µ—Ä–≤—É—é —Å–±–æ—Ä–∫—É —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏ --noconsole –∏ –ª–æ–≥–∏—Ä—É–µ—Ç –ø—Ä–æ—Ü–µ—Å—Å —Å —ç–º–æ–¥–∑–∏.
    –û—à–∏–±–∫–∏ –ø—Ä–∏ —Å–±–æ—Ä–∫–µ –Ω–µ –≤—ã–≤–æ–¥—è—Ç—Å—è –≤ –ª–æ–≥, –µ—Å–ª–∏ –æ–Ω–∏ –Ω–µ –∫—Ä–∏—Ç–∏—á–Ω—ã.

    :param target_script: –ü—É—Ç—å –∫ —Ü–µ–ª–µ–≤–æ–º—É —Å–∫—Ä–∏–ø—Ç—É, –∫–æ—Ç–æ—Ä—ã–π –±—É–¥–µ—Ç —É–ø–∞–∫–æ–≤–∞–Ω —Å –ø–æ–º–æ—â—å—é PyInstaller.
    """
    # –õ–æ–≥–∏—Ä—É–µ–º –Ω–∞—á–∞–ª–æ –ø—Ä–æ—Ü–µ—Å—Å–∞ —Å–±–æ—Ä–∫–∏ —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–º --noconsole
    logger.info('üß∞ –ù–∞—á–∏–Ω–∞–µ–º –ø–µ—Ä–≤—É—é —Å–±–æ—Ä–∫—É —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–º --noconsole')

    try:
        # –í—ã–ø–æ–ª–Ω—è–µ–º —Å–±–æ—Ä–∫—É —Å –ø–æ–º–æ—â—å—é PyInstaller, –ø–µ—Ä–µ–¥–∞–≤–∞—è –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã
        result = subprocess.run(
            ['pyinstaller', '--noconsole', target_script],  # –ö–æ–º–∞–Ω–¥–∞ –¥–ª—è PyInstaller
            check=True,  # –ï—Å–ª–∏ –∫–æ–º–∞–Ω–¥–∞ –∑–∞–≤–µ—Ä—à–∏—Ç—Å—è —Å –æ—à–∏–±–∫–æ–π, –≤–æ–∑–±—É–∂–¥–∞–µ—Ç—Å—è –∏—Å–∫–ª—é—á–µ–Ω–∏–µ
            stdout=subprocess.PIPE,  # –ü–µ—Ä–µ—Ö–≤–∞—Ç—ã–≤–∞–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –≤—ã–≤–æ–¥
            stderr=subprocess.PIPE,  # –ü–µ—Ä–µ—Ö–≤–∞—Ç—ã–≤–∞–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –≤—ã–≤–æ–¥ –æ—à–∏–±–æ–∫
            text=True  # –ü–æ–ª—É—á–∞–µ–º –≤—ã–≤–æ–¥ –≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫
        )

        # –ï—Å–ª–∏ —Å–±–æ—Ä–∫–∞ —É—Å–ø–µ—à–Ω–∞ (returncode 0), –ª–æ–≥–∏—Ä—É–µ–º —É—Å–ø–µ—à–Ω–æ–µ –∑–∞–≤–µ—Ä—à–µ–Ω–∏–µ
        if result.returncode == 0:
            logger.info('‚úÖ –ü–µ—Ä–≤–∞—è —Å–±–æ—Ä–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞ —É—Å–ø–µ—à–Ω–æ.')
        else:
            # –ï—Å–ª–∏ –≤–æ–∑–≤—Ä–∞—â–µ–Ω –∫–æ–¥ –æ—à–∏–±–∫–∏, –ª–æ–≥–∏—Ä—É–µ–º –æ—à–∏–±–∫—É –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ–º –µ—ë –≤ —Ñ–∞–π–ª
            logger.error(f'‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–µ—Ä–≤–æ–π —Å–±–æ—Ä–∫–µ —Å –∫–æ–¥–æ–º {result.returncode}')
            if result.stderr:
                logger.error(f'‚ùå –û—à–∏–±–∫–∏ –ø—Ä–∏ –ø–µ—Ä–≤–æ–π —Å–±–æ—Ä–∫–µ: {result.stderr}')
                with open("build_errors.log", "w") as error_file:  # –°–æ—Ö—Ä–∞–Ω—è–µ–º –æ—à–∏–±–∫–∏ –≤ —Ñ–∞–π–ª
                    error_file.write(result.stderr)
                logger.info(f'üìÇ –û—à–∏–±–∫–∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã –≤ —Ñ–∞–π–ª "build_errors.log".')

    except subprocess.CalledProcessError as e:
        # –õ–æ–≥–∏—Ä—É–µ–º –æ—à–∏–±–∫—É, –µ—Å–ª–∏ PyInstaller –≤—ã–∑–≤–∞–ª –æ—à–∏–±–∫—É –≤ –ø—Ä–æ—Ü–µ—Å—Å–µ —Å–±–æ—Ä–∫–∏
        logger.error(f'‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–µ—Ä–≤–æ–π —Å–±–æ—Ä–∫–µ: {e}')
        exit(1)  # –ó–∞–≤–µ—Ä—à–∞–µ–º –ø—Ä–æ–≥—Ä–∞–º–º—É —Å –∫–æ–¥–æ–º 1 (–æ—à–∏–±–∫–∞)

    except Exception as e:
        # –õ–æ–≥–∏—Ä—É–µ–º –ª—é–±—ã–µ –¥—Ä—É–≥–∏–µ –∏—Å–∫–ª—é—á–µ–Ω–∏—è, –∫–æ—Ç–æ—Ä—ã–µ –º–æ–≥—É—Ç –≤–æ–∑–Ω–∏–∫–Ω—É—Ç—å
        logger.error(f'‚ùå –ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –æ—à–∏–±–∫–∞ –ø—Ä–∏ —Å–±–æ—Ä–∫–µ: {e}')
        exit(1)  # –ó–∞–≤–µ—Ä—à–∞–µ–º –ø—Ä–æ–≥—Ä–∞–º–º—É —Å –∫–æ–¥–æ–º 1 (–æ—à–∏–±–∫–∞)

